                <div class="article-detail" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                    <h3>Scrum</h3>
                    <div class="article-detailLine"></div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <p>Scrum 是一種基於敏捷開發的框架，用於管理複雜產品開發。Scrum 強調的是在小型團隊內進行自組織和交付價值。Scrum 通常由三個主要角色組成：產品負責人、Scrum Master 和開發團隊。開發工作被分成一系列的短期迭代，稱為 Sprint，每個 Sprint 都有一個明確的目標和一個可交付的產品增量。</p>
                        <h4>Scrum 的角色</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>產品負責人：負責產品的願景和範圍</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Scrum 主管：負責幫助團隊遵循 Scrum 的過程</li>
                            <li><i class="fa-regular fa-note-sticky"></i>開發團隊：負責構建產品</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>Scrum 的活動</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint 計劃：團隊計劃下一個 Sprint 要完成的工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint：團隊工作以完成 Sprint 計劃中確定的工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint 回顧：團隊反思 Sprint 的過程並確定改進措施</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint 回顧：團隊展示 Sprint 的成果並獲得利益相關者的反饋</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>Scrum 的工件</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>產品待辦事項列表：包含所有產品需求的列表</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint 待辦事項列表：從產品待辦事項列表中選取的、將在下一 Sprint 中完成的工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>Sprint 燃盡圖：顯示 Sprint 中剩余工作的圖表</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>範例：</h4>
                        <p>一個團隊在 Scrum 中工作，他們可能會每兩週進行一次 Sprint。在 Sprint 規劃會議上，團隊和產品負責人確定該 Sprint 的目標。然後，在每日 Scrum 會議上，團隊成員討論他們的進度、面臨的挑戰以及如何解決它們。在 Sprint 結束時，團隊進行回顧，檢討該如何改進下一次 Sprint。</p>
                        <h4>優點：</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>透明度：透過短期 Sprint 和每日 Scrum 會議，所有人都清楚地知道團隊的進度和目標</li>
                            <li><i class="fa-regular fa-note-sticky"></i>高度自組織：團隊在自組織的環境中工作，能夠更靈活地應對變化</li>
                            <li><i class="fa-regular fa-note-sticky"></i>反饋迅速：每個 Sprint 都有一個可交付的產品增量，使得反饋迅速</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">

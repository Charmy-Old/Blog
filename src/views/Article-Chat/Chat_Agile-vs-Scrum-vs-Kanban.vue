                <div class="article-detail" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                    <h3>Kanban</h3>
                    <div class="article-detailLine"></div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <p>Kanban 是一種流程管理方法，強調在最大化價值交付的同時，減少浪費和提高效率。Kanban 通常使用一個看板來視覺化工作流程，並限制同時進行的工作量，以便更好地控制工作的流動。</p>
                        <h4>Kanban 是包含以下列的看板</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>待辦事項：包含所有待完成的工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>進行中：包含正在進行的工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>已完成：包含已完成的工作</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>Kanban 的原則</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>工作流程可視化</li>
                            <li><i class="fa-regular fa-note-sticky"></i>限制工作量</li>
                            <li><i class="fa-regular fa-note-sticky"></i>管理工作流程</li>
                            <li><i class="fa-regular fa-note-sticky"></i>持續改進</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>範例：</h4>
                        <p>一個團隊使用 Kanban 管理工作流程。他們將工作項目分成不同的階段（例如「待處理」、「進行中」、「待審核」、「完成」等），並在看板上將這些項目可視化。每個階段都有一個限制，例如「進行中」階段可能同時只能進行三項工作。當一項工作完成時，團隊可以開始進行新的工作。</p>
                        <h4>優點：</h4>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>流程可視化：通過看板，團隊可以清楚地看到工作的流動，從而更好地管理和優化流程</li>
                            <li><i class="fa-regular fa-note-sticky"></i>彈性：與 Scrum 不同，Kanban 沒有固定的 Sprint 時間表，團隊可以根據需要隨時添加或刪除工作</li>
                            <li><i class="fa-regular fa-note-sticky"></i>透明度和靈活性：團隊可以根據需求動態調整工作量，以確保優先處理最重要的工作</li>
                        </ul>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <h4>缺點：</h4>
                    </div>                    
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i>可能缺乏結構：相對於 Scrum，Kanban 的流程可能較為靈活，但有時可能會導致缺乏結構</li>
                            <li><i class="fa-regular fa-note-sticky"></i>需要積極管理：Kanban 需要團隊積極管理工作流程，否則可能會變得混亂</li>
                        </ul>
                    </div>
                </div>
            </section>


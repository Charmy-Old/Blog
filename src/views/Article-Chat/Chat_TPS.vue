                        <p>豐田式管理追求的是，以長期持續不斷改進流程與即時生產JIT作為核心理念，降低庫存並大幅壓縮接單到生產交貨的時間，從根本的企業文化架構出從企業管理到實際生產的應用模式。</p>
                        <h4>精實管理：</h4>
                        <p>精實管理則是延續了豐田式管理的零庫存精神，為一種系統化的生產方式，目的在減少生產過程中的無效浪費，核心觀念是投入最少資源，包含人力、設備、資金、材料、時間與空間，創造最大價值。降低浪費與存貨因而降低成本，提高生產效率，快速轉換與少量生產提高生產彈性等，某種程度來說也大幅提高了企業應對危機時的彈性。</p>

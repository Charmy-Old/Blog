                    <h3>豐田式管理的 7 大浪費與新增的第 8 浪費</h3>
                    <div class="article-detailLine"></div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <p>TPS 的主要目標是杜絕一切的浪費，而 TPS 將浪費定義為 7 種與 2003 年新增的第 8 種。</p>
                    </div>
                    <div class="article-list" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <ul>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">等待浪費</h4>：等待即是時間上的浪費，停工待料、檢驗停頓、設備故障都有損耗時間的問題，而時間浪費會影響到實際產能與產線的稼動率多寡</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">搬運浪費</h4>：搬運過程中的堆疊放置、移動與整理，需耗費人時工時，產線的搬運浪費為產品在不同製程間轉換運送，工廠產線的配置模式若不連貫，即造成搬運浪費</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">不良品浪費</h4>：生產過程中製程品質不良而導致產品不良，不論是材料損耗、設備折舊、人工作業瑕疵等，品質不佳的產品容易造成訂單交期延誤，更甚者影響公司信譽</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">動作浪費</h4>：作業動作停止、步行多或重覆不必要動作等，產線在組裝時如有過多動作，容易增加組裝工作勞累程度且降低生產效率，進而影響產能</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">加工浪費</h4>：當產品進行不必要的加工，或要求過高精度，都會導致加工浪費，加工浪費會導致設備過度損耗與折舊問題，過程中的工時損失與人工消耗</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">庫存浪費</h4>：當庫存過多時會造成額外成本，不管是原物料、零組件、半成品與在製品等，或輔助材料，增加倉儲及運送成本，造成儲存空間浪費與資金卡關等問題</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">製造過多或過早的生產浪費</h4>：無法即時生產，製造過多或過早，衍生成半成品數量過多問題，也有成為呆滯品的風險，增加庫存浪費</li>
                            <li><i class="fa-regular fa-note-sticky"></i><h4 style="color: brown; display: inline;">人類智慧的浪費</h4>：在 2003 年定義的第八種浪費，屬於無形的浪費，人才資源的重視與技能再造，讓員工跟著企業成長，但若忽視則讓員工失去改善與學習的機會，也同時失去企業成長的關鍵</li>
                        </ul>

            </section>

            <section>
                <div class="article-detail" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                    <h3>IoT 簡介</h3>
                    <div class="article-detailLine"></div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <p>IoT 全名：Internet of Things，是指連接著各種裝置的集體網路和幫助裝置與雲端和裝置之間互相通訊的技術，這些裝置範圍涵蓋一般家庭物件到精密的工業工具。</p>
                        <p>IoT 已成為 21 世紀最重要的技術之一。現在能夠透過嵌入式裝置，將日常物件(家電、汽車、感測器、監視器等等)連至網路，讓人們、過程和事物之間進行無縫通訊。</p>
                        <p>物聯網將日常的「物」與網路整合在一起。從 90 年代開始就在日常用品上加裝感測器和處理器。但是當時晶片又大又笨重，所以一開始並不流行。被稱為無線射頻辨識(RFID)標籤的低功耗電腦晶片最先被用於追蹤昂貴的設備。隨著科技發展，運算裝置的體積開始縮小，而這些晶片也變得更小更快更聰明。</p>
                        <p>現在，小型物品賦予運算能力的成本現在已大幅下降。例如：可以為嵌入式 RAM 不到 1MB 的 MCU 新增與 Alexa 語音服務連接的能力，並將其用於電燈開關。這些智慧型物品可以自動傳輸資料到網路上或從網路上取得資料。這些「看不見的運算裝置」和相關科技被統稱為物聯網。</p>
                        <h4>IoT 如何運作？</h4>
                        <p>典型的 IoT 系統會隨時收集並交換資料。一個 IoT 系統包含三個部分：</p>
                    </div>
                    <div class="article-txt" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
                        <p><i class="fa-regular fa-note-sticky"></i>智慧型裝置：智慧型裝置具有運算能力，例如電視、監控攝影機或運動裝備都可以是一種智慧型裝置。這些裝置會從四周的環境、使用者的輸入或使用模式中收集資料，並透過網路和 IoT 應用程式交換這些資料</p>
                        <p><i class="fa-regular fa-note-sticky"></i>IoT 應用程式：IoT 應用程式是用於整合來自各個 IoT 裝置資料的服務和軟體的統稱。這些應用程式會使用機器學習或人工智慧 (AI) 技術來分析這些資料並做出聰明的決策。作出的決策會再回傳給 IoT 裝置，讓 IoT 網裝置能更聰明地回應使用者的需求</p>
                        <p><i class="fa-regular fa-note-sticky"></i>圖形化使用者介面：IoT 裝置或機群可以使用圖形化使用者介面管理。常見的範例包括可以註冊並控制智慧型裝置的行動裝置應用程式或網站</p>
                    </div>
                    <div class="article-pic" data-aos="fade-up" data-aos-anchor-placement="top-bottom" data-aos-delay="150" data-aos-duration="1000">
